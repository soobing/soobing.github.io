function IconButtonBar({ links = {} }) {
  const IconPicker = useCallback((icon) => {
    switch (icon) {
      case 'post':
        return <DescriptionIcon />;
      case 'demo':
        return <PlayIcon />;
      case 'github':
        return <GitHubIcon />;
      case 'googlePlay':
        return <AndroidIcon />;
      case 'appStore':
        return <AppleIcon />;
      case 'email':
        return <EmailIcon />;
      case 'linkedIn':
        return <LinkedInIcon />;
      default:
        return null;
    }
  }, []);

  return (
    <div className="icon-button-bar">
      {Object.keys(links).map((link, index) => {
        return (
          links[link] && (
            <a
              key={index}
              className="icon-button"
              href={`${link === 'email' ? `mailto:` : ``}${links[link]}`}
              title={link}
              target={link === 'email' ? undefined : '_blank'}
              rel={link === 'email' ? undefined : 'noopener noreferrer'}
            >
              {IconPicker(link)}
            </a>
          )
        );
      })}
    </div>
  );
}

export default IconButtonBar;
